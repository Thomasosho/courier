     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'track_id' => 'required',
        ]);
        
        $receipt = new Receipt;
        $receipt->track_id = $request->input('track_id');
        $receipt->save();
        return back()->with('success', 'Successfully Saved');
    }

